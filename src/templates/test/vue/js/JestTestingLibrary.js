import { render, screen } from "@testing-library/react";

import { ResourceName } from "componentPath";

describe("ResourceName", () => {
  const componentData = {
    props: {},
    slots: {},
  };

  it("renders the correct message", () => {
    render(ResourceName, componentData);

    expect(
      screen.getByText("Hello, I'm a resource auto generated by Tricorder CLI!")
    ).toBeInTheDocument();
  });
});
